import mongoose from "mongoose";

const StudSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const StudModel = mongoose.model("studinfos", StudSchema);
export default StudModel;
